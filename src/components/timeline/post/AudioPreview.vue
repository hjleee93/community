<template>
    <div class="audio-preview-container">
        <div
            class="audio-preview"
            v-for="(audio, idx) in audioPreviewArr"
            :key="idx"
        >
            <svg class="icon-cross" @click="deletePreviewAudio(idx)">
                <use xlink:href="#svg-cross-thin"></use>
            </svg>
            <audio controls :src="audio.url"></audio>
        </div>
    </div>
</template>

<script lang="ts">
import { Component, Prop, Vue, Watch } from "vue-property-decorator";

import { bus } from "@/main";
@Component({
    components: {},
})
export default class AudioPreview extends Vue {
    private audioPreviewArr: any[] = [];
    private fileLoader: any;

    beforeDestroy() {
        bus.$off("fileLoder");
    }
    mounted() {
        bus.$on("fileLoader", (fileLoader: any) => {
            this.fileLoader = fileLoader;
            this.audioPreviewArr = fileLoader.fileObj.audio;
        });
    }

    //미리보기 사진 삭제
    deletePreviewAudio(idx: number) {
        this.$store.getters.audioArr.splice(idx, 1)
        //
        // this.$store.commit('audioArr', audioArr)
    }

    @Watch('$store.getters.audioArr')
    watchAudio(){
        this.audioPreviewArr = this.$store.getters.audioArr
        console.log('watch', this.$store.getters.audioArr)
    }
}
</script>

