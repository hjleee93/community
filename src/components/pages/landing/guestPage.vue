<template>
    <div>
        <div class="landing-header">
            <p class="header-title">GAMES TO CREATORS</p>
            <p class="header-desc mt-4">
                Game-sharing platform Zempie allows anyone to upload
            </p>
            <p class="header-desc mt-2">
                and share games Upload the game I made and share fun with people
                all around the world
            </p>

        </div>
        <div class="content-grid">
            <div class="landing-container">
                <div class="landing-title">Zempie communities</div>


            </div>
            <div class="grid grid-3-3-3 ">

                <community-card
                    v-for="community in communityList"
                    :key="community.id"
                    :community="community"
                ></community-card>

            </div>
        </div>
    </div>
</template>

<script lang="ts">
import {Component, Prop, Vue} from "vue-property-decorator";
import CommunityCard from "@/components/pages/community/CommunityCard.vue";
import Dropdown from "@/plugins/dropdown";
import {AxiosError} from "axios";
import {scrollDone} from "@/script/scrollManager";
import store from "@/store";

@Component({
    components: {CommunityCard},
})
export default class guestPage extends Vue {
    private communityList: any = [];
    private searchInput: string = "";
    private dropdown: Dropdown = new Dropdown();


    private limit: number = 20;
    private offset: number = 0;
    private community: string = '';
    private sort: string = '';
    private show: string = '';
    private isAddData: boolean = false;

    mounted() {
        this.dropdown.init();
        this.fetch();
    }

    fetch() {
        const obj = {
            limit: this.limit,
            offset: this.offset,
            community: this.searchInput,
            sort: this.sort,
            show: this.show
        }

        this.$api.communityList(obj)
            .then((res: any) => {
                if (this.isAddData) {
                    if (res.length > 0) {
                        this.communityList = [...this.communityList, ...res]
                    }
                    else {
                        window.removeEventListener("scroll", this.scrollCheck);
                    }
                }
                else {
                    this.communityList = res;
                    this.isAddData = true
                }
            })
            .catch((err: AxiosError) => {
                console.log(err)
            })
    }

    searchCommunity() {
        this.$router.push(`/community/list?q=${this.searchInput}`);
    }

    scrollCheck() {
        if (scrollDone(document.documentElement)) {
            this.offset += this.limit;
            this.fetch();
        }
    }
}
</script>

